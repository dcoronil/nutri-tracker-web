function paletteByBmiCategory(category: string): TonePalette {
  const normalized = category.toLowerCase();

  if (normalized.includes("under")) {
    return {
      accent: "#86A6CF",
      contour: "#5B7395",
      badgeBg: "#121721",
      badgeBorder: "#3A4B65",
    };
  }

  if (normalized.includes("normal")) {
    return {
      accent: "#6EE7B7",
      contour: "#4B9E7D",
      badgeBg: "#0F1815",
      badgeBorder: "#36594D",
    };
  }

  if (normalized.includes("over")) {
    return {
      accent: "#D4A24C",
      contour: "#987037",
      badgeBg: "#1A140B",
      badgeBorder: "#5F4722",
    };
  }

  if (normalized.includes("obes")) {
    return {
      accent: "#D17A7A",
      contour: "#975656",
      badgeBg: "#1D1111",
      badgeBorder: "#663A3A",
    };
  }

  return {
    accent: "#B3B5BD",
    contour: "#767A84",
    badgeBg: "#18181B",
    badgeBorder: "#3A3B42",
  };
}

function formatSignedKg(value: number | null): string {
  if (value == null) {
    return "N/D";
  }
  const sign = value > 0 ? "+" : "";
  return `${sign}${value.toFixed(2)} kg`;
}

function clamp(value: number, min: number, max: number): number {
  return Math.min(max, Math.max(min, value));
}

